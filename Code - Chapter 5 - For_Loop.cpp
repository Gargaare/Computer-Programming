#include<iostream>
using namespace std;
int main()
{
	for (int i = 0; i < 10; i++)
	{
		cout << i << " ";
	}
	cout<<endl;
	cout<<"---------------------------"<<endl;
	
	for (int n = 1; n <= 5; n++)
	{
        cout << "Hello!" << endl;
        cout << "*" << endl;
	}
	cout<<"---------------------------"<<endl;
	
	for (int s = 1; s <= 20; s = s + 2)
	{
		cout << " " << s;  
	}
	cout<<endl;
        
    cout<<"---------------------------"<<endl;
	    
    for (int m = 10; m >= 1; m--)
    {
    	cout << " " << m;
	}
        
}


